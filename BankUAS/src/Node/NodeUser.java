package Node;

public class NodeUser {
    public int pk;
    public String namaPengguna;
    public String noRek;
    public String pin;

    public NodeUser(int pk, String namaPengguna, String noRek, String pin) {
        this.pk = pk;
        this.namaPengguna = namaPengguna;
        this.noRek = noRek;
        this.pin = pin;
    }
}
